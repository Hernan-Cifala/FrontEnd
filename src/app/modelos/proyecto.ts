export class Proyecto {

    id : Number;
    titulo : String;
    subtitulo : String;
    descripcion : String;

    constructor(id : Number, titulo : String, subtitulo : String, descripcion : String) {
        this.id = id;
        this.titulo = titulo;
        this.subtitulo = subtitulo;
        this.descripcion = descripcion;
    }

}
